package site.suimu.pojo;

/**
 *
 */
public class ChengCPU {
    private int cpuId;
    private String phone;
    private String name;
    private String brand;
    private String price;
    private String set; // CPU 系列
    private String suitableType;  //适用类型
    private String coreCounts;   //核心数量
    private String slotType;    // 插槽类型
    private String threadCounts; //线程数量
    private String imageUrl;
    private String grade;
    private String link;

    public int getCpuId() {
        return cpuId;
    }

    public void setCpuId(int cpuId) {
        this.cpuId = cpuId;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getSet() {
        return set;
    }

    public void setSet(String set) {
        this.set = set;
    }

    public String getSuitableType() {
        return suitableType;
    }

    public void setSuitableType(String suitableType) {
        this.suitableType = suitableType;
    }

    public String getCoreCounts() {
        return coreCounts;
    }

    public void setCoreCounts(String coreCounts) {
        this.coreCounts = coreCounts;
    }

    public String getSlotType() {
        return slotType;
    }

    public void setSlotType(String slotType) {
        this.slotType = slotType;
    }

    public String getThreadCounts() {
        return threadCounts;
    }

    public void setThreadCounts(String threadCounts) {
        this.threadCounts = threadCounts;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
